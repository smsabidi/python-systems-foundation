# Single value
print("Hello, World!")

# Multiple Values
name = "Riya"
age = 20
print("Name:", name, "| Age:", age)
print("Hello " + name + " your age is " + str(age))
print(name, age)
print(name, age, sep="-")
print(name, end=" ")  # By default new line
print(age)

# F-String
print(f"name is {name} and my age is {age}")
# Printing number and expression
print(10 + 5)
print(type(3.14))
